#include "libft.h"

size_t	print_s(char *s)
{
	size_t	i;

	i = 0;
	if (!s)
		return (print_s("(null)"));
	while (s[i])
	{
		write (1, &s[i], 1);
		i++;
	}
	return (i);
}
