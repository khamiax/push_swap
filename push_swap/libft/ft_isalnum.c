#include "libft.h"

int	ft_isalnum(int *c)
{
	int	i;

	i = 0;
	while (c[i])
	{
		if (!((c[i] >= 65 && c[i] <= 90)
				|| (c[i] >= 97 && c[i] <= 122) || (c[i] >= 48 && c[i] <= 57)))
			return (0);
	}
	return (1);
}
